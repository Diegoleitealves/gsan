public class RelatorioManterArrecadacaoFormaBean implements RelatorioBean {
	
	private String id;
	
	private String codigo;

	private String descricao;

	/**
	 * Construtor da classe RelatorioManterArrecadacaoFormaBean
	 * 
	 * @param codigo
	 *            Descrição do parâmetro
	 * @param gerenciaRegional
	 *            Descrição do parâmetro
	 * @param nome
	 *            Descrição do parâmetro
	 * @param 
	 *            Descrição do parâmetro
	 */

	public RelatorioManterArrecadacaoFormaBean(String id, String codigo, String descricao) {
		
		this.id = id;
		this.codigo = codigo;
		this.descricao = descricao;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
