package gcom.faturamento.bean;


public class LinhaFaixaConsumoContaHelper {
	
	private String faixaConsumo;
	private String tarifaConsumo;
	private String valor;
	
	/**
	 * @return Retorna o campo faixaConsumo.
	 */
	public String getFaixaConsumo() {
		return faixaConsumo;
	}
	/**
	 * @param faixaConsumo O faixaConsumo a ser setado.
	 */
	public void setFaixaConsumo(String faixaConsumo) {
		this.faixaConsumo = faixaConsumo;
	}
	/**
	 * @return Retorna o campo tarifaConsumo.
	 */
	public String getTarifaConsumo() {
		return tarifaConsumo;
	}
	/**
	 * @param tarifaConsumo O tarifaConsumo a ser setado.
	 */
	public void setTarifaConsumo(String tarifaConsumo) {
		this.tarifaConsumo = tarifaConsumo;
	}
	/**
	 * @return Retorna o campo valor.
	 */
	public String getValor() {
		return valor;
	}
	/**
	 * @param valor O valor a ser setado.
	 */
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	
	
	
}
